package com.corgi.ninemensmorris.Enum;

/**
 *
 * This enum represents the state of the player.
 * PLACING: The player is placing a token on the board.
 * MOVING: The player is moving a token on the board.
 * FLYING: The player is flying a token on the board.
 *
 */
public enum PlayerState {
    PLACING,
    MOVING,
    FLYING
}
